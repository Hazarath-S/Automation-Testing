public class If_elseif_block {

	public static void main(String[] args) {
		
		int operant1 = 200;
		int operant2 = 500;
		
		if(operant1>operant2) 
		{
			System.out.println("My Code is Correct");		
		}
		else if (operant1<=operant2)
		{
			System.out.println("My Code is Swithed");		
		}
		else
		{
			System.out.println("Condition is not matched");
		}
	}
}
